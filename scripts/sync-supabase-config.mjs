/**
 * 从项目根目录 .env 读取 Supabase 变量并写入 config.js（供静态 HTML 使用）
 * 支持：VITE_SUPABASE_URL / VITE_SUPABASE_ANON_KEY
 *      或 SUPABASE_URL / SUPABASE_ANON_KEY
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

function parseEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return {};
  const raw = fs.readFileSync(filePath, "utf8");
  const out = {};
  for (const line of raw.split(/\n/)) {
    const t = line.trim();
    if (!t || t.startsWith("#")) continue;
    const i = t.indexOf("=");
    if (i === -1) continue;
    const k = t.slice(0, i).trim();
    let v = t.slice(i + 1).trim();
    if (
      (v.startsWith('"') && v.endsWith('"')) ||
      (v.startsWith("'") && v.endsWith("'"))
    ) {
      v = v.slice(1, -1);
    }
    out[k] = v;
  }
  return out;
}

const env = parseEnvFile(path.join(root, ".env"));
const url =
  env.VITE_SUPABASE_URL ||
  env.SUPABASE_URL ||
  "";
const key =
  env.VITE_SUPABASE_ANON_KEY ||
  env.SUPABASE_ANON_KEY ||
  "";

const banner = `/* Generated by npm run sync-supabase-config — do not commit real keys to a public repo */
`;

const body = `${banner}window.AETHER_CONFIG = {
  supabaseUrl: ${JSON.stringify(url)},
  supabaseAnonKey: ${JSON.stringify(key)},
};
`;

const outPath = path.join(root, "config.js");
fs.writeFileSync(outPath, body, "utf8");

if (url && key) {
  console.log("config.js 已写入：已检测到 Supabase URL 与 anon key。");
} else {
  console.warn(
    "config.js 已写入，但 .env 中缺少 VITE_SUPABASE_URL 或 VITE_SUPABASE_ANON_KEY（或 SUPABASE_*）。登录/注册需填写后重新运行本命令。"
  );
}
